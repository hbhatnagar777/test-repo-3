# Module for Dynamics 365 Web API operations
#   File description:
#       constants:      Authentication URL;s, static URL's, API URL's etc.
#       d365_entity:    Class denoting a single record. operations include,
#                       read, modify, delete a record, fetch metadata etc
#       d365_env:       Denotes an environment in Dynamics 365. Operations include, list tables etc
#       d365_org:       Denoting an organization. Operations include: fetching a list of CRM environments
#       web_auth:       Authentication of Web API's
#       web_req:        Making a web request to the Dynamics 365 Web API's
#       API reference:  https://docs.microsoft.com/en-us/power-apps/developer/data-platform/webapi/overview
